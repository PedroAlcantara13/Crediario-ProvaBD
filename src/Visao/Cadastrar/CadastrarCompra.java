    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nome = comboCli.getSelectedItem().toString();
        String preco = jTF_Produto.getText();
        String produto = comboPro.getSelectedItem().toString();

        if (nome.equals("") || produto.equals("") || preco.equals("")) {

            JOptionPane.showMessageDialog(null, "Nenhum campo estÃ¡ vazio",
                "VÃ­deo Locadora", JOptionPane.WARNING_MESSAGE);
        } else {
            Connection con = Conexao.AbrirConexao();
            CompraDAO sql = new CompraDAO(con);
            int n = Integer.parseInt(preco);
            Compra a = new Compra();

            a.setNomecli(nome);
            a.setProduto(produto);
            a.setDivida(n);

            sql.Inserir_Compra(a);
            Conexao.FecharConexao(con);

            jTF_Cliente.setText("");
            jTF_Produto.setText("");
            comboPro.setSelectedIndex(0);
            comboCli.setSelectedIndex(0);
           

            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso",
                "VÃ­deo Locadora", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

